package hashing_test

import (
	"bytes"
	"testing"

	"github.com/copperexchange/knox-primitives/pkg/core/hashing"
	"github.com/stretchr/testify/require"
)

// TODO: run PRNG statistical tests on the output of this hash.
func TestHashAes(t *testing.T) {
	// Hardcode multiple input data strings to hash, of sizes %BlockSize == 0.
	testInputs := []string{
		"0123456789ABCDEF0123456789ABCDEF",
		"He who controls the spice controls the universe.",
		"Lorem ipsum dolor sit amet, consectetur adipiscing elit, sed do eiusmod tempor incididunt ut labore et dolore magna aliqua.",
	}
	testOutputLengths := []int{1, 2, 4}
	// Hardcode the expected outputs for the test inputs.
	expectedDigests := [][]byte{
		{
			0x07, 0xc5, 0xf1, 0x3c, 0xd8, 0x86, 0x04, 0x62,
			0x30, 0xaf, 0x71, 0x98, 0xe8, 0xbc, 0x45, 0x16,
		},
		{
			0xe5, 0xb4, 0x4d, 0xe0, 0xe7, 0xcc, 0xdb, 0xe5,
			0x47, 0x89, 0xe5, 0xaa, 0x9a, 0xe0, 0x26, 0xe8,
			0x29, 0x0b, 0xf5, 0x31, 0x9f, 0xfb, 0xd6, 0xb7,
			0xa6, 0x71, 0xaf, 0x29, 0x59, 0x2f, 0x57, 0x5d,
		},
		{
			0x40, 0x96, 0x58, 0x61, 0x75, 0x9e, 0xf8, 0x74,
			0x2b, 0xc2, 0xe8, 0xe9, 0x91, 0x12, 0xc0, 0xc2,
			0xb9, 0xa6, 0x5b, 0xde, 0x9c, 0xb5, 0xd8, 0x12,
			0x35, 0x74, 0x3f, 0xd5, 0xb9, 0xe7, 0xf7, 0x07,
			0x50, 0x99, 0x15, 0xd8, 0x41, 0x57, 0xe6, 0xf7,
			0xf1, 0xf7, 0x83, 0x4b, 0xc0, 0x45, 0xc1, 0xb1,
			0x84, 0xf8, 0xf8, 0x0d, 0x66, 0x34, 0x2d, 0x12,
			0x44, 0xb4, 0x4d, 0x1d, 0x97, 0x1b, 0x77, 0x1b,
		},
	}
	// Hash each input and compare the output with the expected output.
	sessionId := []byte("ThisIsOne32BytesSessionIdExample")
	for i, input := range testInputs {
		inputBytes := []byte(input)
		digest, err := hashing.HashAes(inputBytes, sessionId, testOutputLengths[i])
		require.NoError(t, err)
		require.True(t, bytes.Equal(digest, expectedDigests[i]))
	}
}
