func TestJacobiTable(t *testing.T) {
	t.Parallel()
	for n := uint64(1); n < 60; n++ {
		nIndex := n / 2
		for aSubOne, expected := range jacobiTable[nIndex] {
			a := int64(aSubOne) + 1
			t.Run(fmt.Sprintf("a=%d, n=%d", a, n), func(t *testing.T) {
				t.Parallel()
				nn, err := num.NPlus().FromUint64(n)
				require.NoError(t, err)
				out, err := nt.Jacobi(num.Z().FromInt64(a), nn)
				if n%2 == 1 {
					require.NoError(t, err)
					require.Equal(t, expected, out, "Jacobi(%d, %d) should be %d", a, n, expected)
				} else {
					require.Error(t, err, "Jacobi symbol should not be defined for even n=%d", n)
				}
			})
		}
	}
}

// TestJacobiTableNegative derives expected values for negative a from the
// positive-a table via the identity Jacobi(-a, n) = Jacobi(a, n) * (-1/n),
// where (-1/n) = +1 if n ≡ 1 (mod 4) and -1 if n ≡ 3 (mod 4).
func TestJacobiTableNegative(t *testing.T) {
	t.Parallel()
	for n := uint64(1); n < 60; n += 2 {
		nIndex := n / 2
		minusOneSymbol := 1
		if n%4 == 3 {
			minusOneSymbol = -1
		}
		for aSubOne, positiveExpected := range jacobiTable[nIndex] {
			a := int64(aSubOne) + 1
			expected := positiveExpected * minusOneSymbol
			t.Run(fmt.Sprintf("a=-%d, n=%d", a, n), func(t *testing.T) {
				t.Parallel()
				nn, err := num.NPlus().FromUint64(n)
				require.NoError(t, err)
				out, err := nt.Jacobi(num.Z().FromInt64(-a), nn)
				require.NoError(t, err)
				require.Equal(t, expected, out, "Jacobi(%d, %d) should be %d", -a, n, expected)
			})
		}
	}
}
