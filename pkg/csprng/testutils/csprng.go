package testutils

import (
	"bytes"
	"testing"

	"github.com/stretchr/testify/require"

	"github.com/copperexchange/krypton-primitives/pkg/csprng"
)

func Test_prng(t *testing.T, prngGenerator func(seed, salt []byte) (csprng.CSPRNG, error)) {
	t.Helper()
	// hardoded random 32B keys
	keys := [][]byte{
		{0x0f, 0x0e, 0x0d, 0x0c, 0x0b, 0x0a, 0x09, 0x08, 0x07, 0x06, 0x05, 0x04, 0x03, 0x02, 0x01, 0x00,
			0x0f, 0x0e, 0x0d, 0x0c, 0x0b, 0x0a, 0x09, 0x08, 0x07, 0x06, 0x05, 0x04, 0x03, 0x02, 0x01, 0x00},
		{0xf3, 0x52, 0x5d, 0x6c, 0x7b, 0x8a, 0x99, 0xa8, 0xb7, 0xc6, 0xd5, 0xe4, 0xf3, 0x52, 0x5d, 0x6c,
			0x7b, 0x8a, 0x99, 0xa8, 0xb7, 0xc6, 0xd5, 0xe4, 0xf3, 0x52, 0x5d, 0x6c, 0x7b, 0x8a, 0x99, 0xa8},
	}
	// hardoded random 32B nonces
	nonces := [][]byte{
		{0x59, 0x58, 0x57, 0x56, 0x55, 0x54, 0x53, 0x00, 0x51, 0x50, 0x4f, 0x4e, 0x4d, 0x00, 0x4b, 0x4a,
			0x49, 0x48, 0x47, 0x00, 0x45, 0x44, 0x43, 0x42, 0x41, 0x00, 0x3f, 0x3e, 0x3d, 0x00, 0x3b, 0x3a},
		{0x3f, 0x3e, 0x3d, 0x00, 0x3b, 0x3a, 0x39, 0x38, 0x37, 0x00, 0x35, 0x34, 0x33, 0x32, 0x31, 0x00,
			0x2f, 0x2e, 0x2d, 0x00, 0x2b, 0x2a, 0x29, 0x28, 0x27, 0x00, 0x25, 0x24, 0x23, 0x00, 0x21, 0x20},
	}
	for i := range keys {
		// create a new PRNG
		prng, err := prngGenerator(keys[i], nonces[i])
		require.NoError(t, err)
		// generate 100B of buffer data
		buffer := make([]byte, 100)
		_, err = prng.Read(buffer)
		require.NoError(t, err)
		// Reset and generate same 120B of buffer data
		buffer2 := make([]byte, 120)
		err = prng.Seed(keys[i], nonces[i])
		require.NoError(t, err)
		_, err = prng.Read(buffer2)
		require.NoError(t, err)
		// Create anew generate 200B of buffer data. Check equality of first 120B
		buffer3 := make([]byte, 200)
		prng2, err := prngGenerator(keys[i], nonces[i])
		require.NoError(t, err)
		_, err = prng2.Read(buffer3)
		require.NoError(t, err)
		// Seed with a different key and nonce and generate 200B of buffer data.
		buffer4 := make([]byte, 200)
		randomKey := make([]byte, 32)
		randomNonce := make([]byte, 16)
		err = prng.Seed(randomKey, randomNonce)
		require.NoError(t, err)
		_, err = prng.Read(buffer4)
		require.NoError(t, err)
		// compare with expected
		require.True(t, bytes.Equal(buffer, buffer2[:100]))
		require.True(t, bytes.Equal(buffer2, buffer3[:120]))
		require.False(t, bytes.Equal(buffer, buffer4[:100]))
		require.False(t, bytes.Equal(buffer2, buffer4[:120]))
		require.False(t, bytes.Equal(buffer3, buffer4))

	}
}
